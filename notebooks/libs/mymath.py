def adder(a, b):
    """
        Function adds values from args a and b^2
    """
    return a+(b*b)

def multiplier(a, b):
    """
        Function multiplies values from args a and b^2
    """
    return a*(b*b)

def divider(a, b):
    """
        Function divides values from args a and b^2
    """
    return a/(b*b)